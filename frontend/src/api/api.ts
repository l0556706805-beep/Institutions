import axios from "axios";

// Get API URL from runtime config (loaded in index.html) or fallback to env var or /api
const getApiUrl = () => {
  if (typeof window !== 'undefined' && (window as any).APP_CONFIG?.API_URL) {
    return (window as any).APP_CONFIG.API_URL;
  }
  return process.env.REACT_APP_API_URL || "/api";
};

// ×™×¦×™×¨×ª ××•×¤×¢ API
const api = axios.create({
  baseURL: getApiUrl(),
});

// ×¤×•× ×§×¦×™×” ×©××’×“×™×¨×” ××ª ×”×˜×•×§×Ÿ ×‘×’×œ×•×‘×œ
export const setAuthToken = (token: string | null) => {
  if (token) {
    api.defaults.headers.common["Authorization"] = `Bearer ${token}`;
    localStorage.setItem("token", token);
  } else {
    delete api.defaults.headers.common["Authorization"];
    localStorage.removeItem("token");
  }
};

// ğŸ”¥ ×‘×¢×ª ×˜×¢×™× ×ª ×”××¤×œ×™×§×¦×™×” â€” ×× ×™×© ×˜×•×§×Ÿ ×‘Ö¾localStorage × ×˜×¢×Ÿ ××•×ª×•
const storedToken = localStorage.getItem("token");
if (storedToken) {
  api.defaults.headers.common["Authorization"] = `Bearer ${storedToken}`;
}

// â›” ×˜×™×¤×•×œ ××•×˜×•××˜×™ ×‘×©×’×™××ª 401 â€” ×˜×•×§×Ÿ ×œ× ×ª×§×£ / ×¤×’
api.interceptors.response.use(
  (response) => response,
  (error) => {
    if (error.response?.status === 401) {
      // ××•×—×§ ×˜×•×§×Ÿ ×œ× ×ª×§×™×Ÿ
      setAuthToken(null);
    }

    return Promise.reject(error);
  }
);

export default api;
